-- ═══════════════════════════════════════════════════════════════════════
--  01_Security.sql  |  보안 및 인증 테이블 생성
--
--  [설계 배경]
--    ALM 은 1인용 로컬 애플리케이션입니다.
--    별도 회원 테이블 없이 단 하나의 마스터 비밀번호만 존재하며,
--    이 테이블에는 항상 행(row)이 정확히 1개만 들어갑니다.
--
--  [보안 설계 원칙]
--    비밀번호 평문(plain text)을 절대 저장하지 않습니다.
--    Java SecurityUtil 클래스에서 SHA-256 단방향 해시로 변환한 후
--    그 결과값(64자리 16진수 문자열)만 DB 에 저장합니다.
--    → 해시는 역산 불가: DB 가 유출돼도 원본 비밀번호를 알 수 없음
-- ═══════════════════════════════════════════════════════════════════════

USE alm_db;

-- ── user_security 테이블 ──────────────────────────────────────────────
-- PRIMARY KEY : 테이블의 각 행을 고유하게 식별하는 컬럼
--   여기서는 password_hash 자체가 PK 입니다.
--   행이 1개뿐이므로 AUTO_INCREMENT ID 가 필요 없고,
--   해시값이 곧 고유 식별자 역할을 합니다.
--
-- VARCHAR(255) : 가변 길이 문자열. SHA-256 결과는 64자이지만
--   여유를 두어 255 로 선언 (다른 해시 알고리즘으로 교체 시 대비)
--
-- DATETIME : 날짜+시간 형식 (예: 2025-05-20 14:30:00)
--   NULL 허용 — 최초 비밀번호 설정 직후에는 아직 로그인 이력이 없으므로
--   last_login_date 가 NULL 인 상태가 정상입니다.
CREATE TABLE user_security (
    password_hash   VARCHAR(255) NOT NULL,  -- SHA-256 해시값 (64자 16진수)
    last_login_date DATETIME,               -- 마지막 로그인 일시 (최초엔 NULL)
    PRIMARY KEY (password_hash)             -- 해시값 자체가 기본키
);

SELECT '✅ 01 보안 테이블(user_security) 생성 완료' AS result;
