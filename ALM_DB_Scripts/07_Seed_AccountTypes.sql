-- ═══════════════════════════════════════════════════════════════════════
--  07_Seed_AccountTypes.sql  |  계좌 종류 기초(Seed) 데이터 삽입
--
--  [account_type 테이블의 역할]
--    계좌 등록 화면의 "계좌 종류" 드롭다운 데이터 소스.
--    사용자가 "보통예금인지, 적금인지, 증권계좌인지"를 선택할 수 있게 합니다.
--
--  ★★★ 증권위탁계좌 항목이 매우 중요합니다 ★★★
--    type_name = '증권위탁계좌' 인 account_type 은
--    투자 포트폴리오(invest_portfolio) 연동 대상 계좌를 식별하는 기준입니다.
--
--    Java AssetService / InvestService 에서 이 이름(문자열)으로 계좌를 조회합니다:
--      SELECT a.* FROM account_table a
--      JOIN account_type t ON a.type_id = t.type_id
--      WHERE t.type_name = '증권위탁계좌'
--
--    → 이 문자열을 임의로 수정하면 투자 기능이 동작하지 않습니다.
--      수정 시 Java 코드의 관련 쿼리도 함께 변경해야 합니다.
--
--  [type_id 자동 부여 순서]
--    AUTO_INCREMENT 이므로 삽입 순서대로 1부터 부여됩니다.
--    '증권위탁계좌' 는 9번째 삽입 = type_id 9 (변경 주의)
-- ═══════════════════════════════════════════════════════════════════════

USE alm_db;

-- ── 계좌 종류 목록 삽입 ───────────────────────────────────────────────
-- type_name 컬럼만 명시 → type_id 는 AUTO_INCREMENT 로 자동 부여
INSERT INTO account_type (type_name) VALUES
('보통예금(입출금)'),       -- type_id 1  : 수시 입출금 가능한 일반 통장
('정기예금'),               -- type_id 2  : 만기까지 묶이는 예금
('정기적금'),               -- type_id 3  : 매월 일정 금액 납입 적금
('자유적금'),               -- type_id 4  : 납입액·시기를 자유롭게 선택
('CMA'),                    -- type_id 5  : 증권사 연계 수시 입출금
('ISA(개인종합자산관리)'),  -- type_id 6  : 세제 혜택 통합 계좌
('주택청약저축'),           -- type_id 7  : 아파트 청약 전용
('외화예금'),               -- type_id 8  : 달러·엔·유로 등 외화 보관
('증권위탁계좌'),           -- type_id 9  : ★ 주식 매매 계좌 → invest_portfolio 연동 대상
('파킹통장'),               -- type_id 10 : 단기 자금 보관, 높은 수시 이율
('마이너스통장'),           -- type_id 11 : 마이너스 잔액(대출) 가능 통장
('대출계좌'),               -- type_id 12 : 대출 잔액 관리용 (부채 개념)
('선불충전금(페이)'),       -- type_id 13 : 카카오페이·토스페이 등 충전 잔액
('포인트/캐시');             -- type_id 14 : 네이버 포인트·쿠팡캐시 등

SELECT '✅ 07 계좌 종류 시드 데이터 삽입 완료 (증권위탁계좌 = type_id 9)' AS result;
